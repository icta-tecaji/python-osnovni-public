# Napišite funkcijo `funkcija02`, ki prejme list.

# Vsak element v listu je tuple. Prvi element v tuplu je znamka avta.
# Drugi element je leto prve registracije avta. Tretji element je
# število prevoženih kilometrov.

# Funkcija naj vrne list znamk avtomobilov, ki imajo leto prve
# registracije večje od 2015 in število prevoženih kilometrov manjše od 200 000.

# ```python
# INPUT:
# l = [("ŠKODA OCTAVIA", 2015, 265_000), ("VOLKSWAGEN PASSAT", 2014, 199_000),
#      ("RENAULT CLIO", 2016, 211_000), ("CITROEN XSARA PICASSO", 2017, 120_000),
#      ("BMW 318", 2016, 300_000), ("KIA CEED", 2016, 154_000)]

# print(funkcija02(l))


# OUTPUT:
# ['CITROEN XSARA PICASSO', 'KIA CEED']
# ```
def funkcija02(l):
    result = []
    for car in l:
        if car[1] > 2015 and car[2] < 200000:
            result.append(car)
    return result
